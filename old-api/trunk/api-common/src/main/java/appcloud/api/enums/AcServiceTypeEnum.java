package appcloud.api.enums;

public enum AcServiceTypeEnum {
	UNKNOWN,RESOURCE_SCHEDULER, VOLUME_SCHEDULER, VOLUME_PROVIDER, VM_SCHEDULER, 
	VM_CONTROLLER, IMAGE_SERVER, NODE_MONITOR, NETWORK_PROVIDER, DHCP_CONTROLLER,
	LOL_SERVER,FLOW_CONTROLLER;
	public String toString() {
		switch (this) {
		case RESOURCE_SCHEDULER:
			return "RESOURCE_SCHEDULER";
		case VOLUME_SCHEDULER:
			return "VOLUME_SCHEDULER";
		case VOLUME_PROVIDER:
		    return "VOLUME_PROVIDER";
		case VM_SCHEDULER:
		    return "VM_SCHEDULER";
		case VM_CONTROLLER:
		    return "VM_CONTROLLER";	
		case FLOW_CONTROLLER:
		    return "FLOW_CONTROLLER";	
		case IMAGE_SERVER:
			return "IMAGE_SERVER";
		case NODE_MONITOR:
			return "NODE_MONITOR";
		case UNKNOWN:
			return "UNKNOWN";
		case DHCP_CONTROLLER:
			return "DHCP_CONTROLLER";
		case NETWORK_PROVIDER:
			return "NETWORK_PROVIDER";
		case LOL_SERVER:
			return "LOL_SERVER";
		}
		return super.toString();
	}
	
	public String serviceName() {
		switch (this) {
		case RESOURCE_SCHEDULER:
			return "resource-scheduler";
		case VOLUME_SCHEDULER:
			return "volume-scheduler";
		case VOLUME_PROVIDER:
		    return "volume-provider";
		case VM_SCHEDULER:
		    return "vm-scheduler";
		case VM_CONTROLLER:
		    return "vm-controller";
		case FLOW_CONTROLLER:
		    return "flow-controller";	
		case IMAGE_SERVER:
			return "image-server";
		case NODE_MONITOR:
			return "node-monitor";
		case DHCP_CONTROLLER:
			return "dhcp-controller";
		case NETWORK_PROVIDER:
			return "network-provider";
		case LOL_SERVER:
			return "lol-server";
		case UNKNOWN:
			return "unknown";
		}
		return super.toString();
	}
}
