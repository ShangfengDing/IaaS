package com.appcloud.vm.fe.entity;

import java.util.List;

public class DiskInventory {

    private String diskId;
    private String diskName;
    private String diskType;
    private Integer diskSize;
    private List<String> diskSnapshotIds;

	public DiskInventory(String diskId, String diskName,String diskType,Integer diskSize,
			List<String> diskSnapshotIds) {
		super();
		this.diskId = diskId;
		this.diskName = diskName;
		this.diskType = diskType;
		this.diskSize = diskSize;
		this.diskSnapshotIds = diskSnapshotIds;
	}
	public DiskInventory() {
		super();
	}

	public String getDiskId() {
		return diskId;
	}
	public void setDiskId(String diskId) {
		this.diskId = diskId;
	}
	public String getDiskName() {
		return diskName;
	}
	public void setDiskName(String diskName) {
		this.diskName = diskName;
	}
	public String getDiskType() {
		return diskType;
	}
	public void setDiskType(String diskType) {
		this.diskType = diskType;
	}
	public List<String> getDiskSnapshotIds() {
		return diskSnapshotIds;
	}
	public void setDiskSnapshotIds(List<String> diskSnapshotIds) {
		this.diskSnapshotIds = diskSnapshotIds;
	}
	public Integer getDiskSize() {
		return diskSize;
	}
	public void setDiskSize(Integer diskSize) {
		this.diskSize = diskSize;
	}

}
