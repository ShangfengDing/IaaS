package com.appcloud.vm.fe.entity;

import java.util.Date;

/**
 * 此为云快照的实体类,状态没有用
 * @author rain
 *
 */
public class ShotStatus {
	/**
	 * 地域的regionId
	 */
	private String regionId;
	/**
	 * 云快照的Id
	 */
	private String shotId;
	/**
	 *云快照的名称 
	 */
	private String shotName;
	/**
	 * 云硬盘Id
	 */
	private String diskId;
	/**
	 * 云硬盘名称
	 */
	private String diskName;
	/**
	 * 云硬盘挂载主机的名字
	 */
	private String instanceName;
	/**
	 * 云镜像大小
	 */
	private Integer shotSize;
	/**
	 * 云硬盘种类
	 */
	private String diskType;
	/**
	 * 创建时间
	 */
	private String createTime;
	/**
	 * 云镜像进度
	 */
	private String shotRate;
	/**
	 * 云镜像的状态
	 */
	private String shotStatus;
	/**
	 * 提供商英文
	 */
	private String providerEn;
	/**
	 * 云镜像用户Id
	 */
	private String userEmail;
	/**
	 * 云镜像的账户名
	 */
	private String appName;
	

	private ShotStatus(){};

	public ShotStatus(String regionId,String shotId,String shotName,String diskId, String diskName, 
			String instanceName, Integer shotSize, String diskType,
			String createTime,String shotRate, String shotStatus,String providerEn, String userEmail,String appName){
		this.regionId = regionId;
		this.shotId = shotId;
		this.shotName = shotName;
		this.diskId = diskId;
		this.diskName = diskName;
		this.instanceName = instanceName;
		this.shotSize = shotSize;
		this.diskType = diskType;
		this.createTime = createTime;
		this.shotRate = shotRate;
		this.shotStatus = shotStatus;
		this.providerEn = providerEn;
		this.userEmail = userEmail;
		this.appName = appName;
	}

	public String getRegionId() {
		return regionId;
	}

	public void setRegionId(String regionId) {
		this.regionId = regionId;
	}

	public String getShotId() {
		return shotId;
	}

	public void setShotId(String shotId) {
		this.shotId = shotId;
	}

	public String getShotName() {
		return shotName;
	}

	public void setShotName(String shotName) {
		this.shotName = shotName;
	}

	public String getDiskId() {
		return diskId;
	}

	public void setDiskId(String diskId) {
		this.diskId = diskId;
	}

	public String getDiskName() {
		return diskName;
	}

	public void setDiskName(String diskName) {
		this.diskName = diskName;
	}

	public Integer getShotSize() {
		return shotSize;
	}

	public void setShotSize(Integer shotSize) {
		this.shotSize = shotSize;
	}

	public String getDiskType() {
		return diskType;
	}

	public void setDiskType(String diskType) {
		this.diskType = diskType;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getShotRate() {
		return shotRate;
	}

	public void setShotRate(String shotRate) {
		this.shotRate = shotRate;
	}

	public String getShotStatus() {
		return shotStatus;
	}

	public void setShotStatus(String shotStatus) {
		this.shotStatus = shotStatus;
	}

	public String getProviderEn() {
		return providerEn;
	}

	public void setProviderEn(String providerEn) {
		this.providerEn = providerEn;
	}

	public String getUserEmail() {
		return userEmail;
	}

	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}

	
	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getInstanceName() {
		return instanceName;
	}

	public void setInstanceName(String instanceName) {
		this.instanceName = instanceName;
	}
	
}
