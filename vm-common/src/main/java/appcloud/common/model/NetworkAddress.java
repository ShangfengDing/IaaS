public class NetworkAddress {

    private String ip;
    private String mac;
    
    public NetworkAddress() {
		super();
	}

	/**
    * <p>Title: </p>
    * <p>Description: </p>
    * @param ip
    * @param mac
    */ 
    public NetworkAddress(String mac, String ip) {
        super();
        this.ip = ip;
        this.mac = mac;
    }

    public String getIp() {
        return ip;
    }
    
    public void setIp(String ip) {
        this.ip = ip;
    }
    
    public String getMac() {
        return mac;
    }
    
    public void setMac(String mac) {
        this.mac = mac;
    }

	@Override
	public String toString() {
		return "NetworkAddress [ip=" + ip + ", mac=" + mac + "]";
	}
}
