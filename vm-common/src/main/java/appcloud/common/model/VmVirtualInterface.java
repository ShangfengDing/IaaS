package appcloud.common.model;

public class VmVirtualInterface {
    private Integer id;
    private String instanceUuid;
    private String address;
    private String mac;
    private String networkType;
    private Integer ipPoolId;

    private VmInstance vmInstance;

    public VmVirtualInterface() {
        super();
    }

    public VmVirtualInterface(Integer id,
                              String instanceUuid,
                              String address,
                              String mac,
                              String networkType,
                              Integer ipPoolId) {
        super();
        this.id = id;
        this.instanceUuid = instanceUuid;
        this.address = address;
        this.mac = mac;
        this.networkType = networkType;
        this.ipPoolId = ipPoolId;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getInstanceUuid() {
        return instanceUuid;
    }

    public void setInstanceUuid(String instanceUuid) {
        this.instanceUuid = instanceUuid;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getNetworkType() {
        return networkType;
    }

    public void setNetworkType(String networkType) {
        this.networkType = networkType;
    }

    public Integer getIpPoolId() {
        return ipPoolId;
    }

    public void setIpPoolId(Integer ipPoolId) {
        this.ipPoolId = ipPoolId;
    }

    public VmInstance getVmInstance() {
        return vmInstance;
    }

    public void setVmInstance(VmInstance vmInstance) {
        this.vmInstance = vmInstance;
    }

    @Override
    public String toString() {
        return "VmVirtualInterface [id="
               + id
               + ", instanceUuid="
               + instanceUuid
               + ", address="
               + address
               + ", mac="
               + mac
               + ", networkType="
               + networkType
               + ", ipPoolId="
               + ipPoolId
               + ", vmInstance="
               + vmInstance
               + "]";
    }

}
