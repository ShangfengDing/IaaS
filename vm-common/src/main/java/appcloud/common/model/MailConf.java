package appcloud.common.model;

import java.util.Map;

public class MailConf {
	String lolEmail;
	String emailHost;
	String emailFrom;
	String emailPassword;
	String emailSubject;
	Map<String, String> moduleInCharge;
	
	public String getLolEmail() {
		return lolEmail;
	}
	public void setLolEmail(String lolEmail) {
		this.lolEmail = lolEmail;
	}
	public String getEmailHost() {
		return emailHost;
	}
	public void setEmailHost(String emailHost) {
		this.emailHost = emailHost;
	}
	public String getEmailFrom() {
		return emailFrom;
	}
	public void setEmailFrom(String emailFrom) {
		this.emailFrom = emailFrom;
	}
	public String getEmailPassword() {
		return emailPassword;
	}
	public void setEmailPassword(String emailPassword) {
		this.emailPassword = emailPassword;
	}
	public String getEmailSubject() {
		return emailSubject;
	}
	public void setEmailSubject(String emailSubject) {
		this.emailSubject = emailSubject;
	}
	public Map<String, String> getModuleInCharge() {
		return moduleInCharge;
	}
	public void setModuleInCharge(Map<String, String> moduleInCharge) {
		this.moduleInCharge = moduleInCharge;
	}
	@Override
	public String toString() {
		return "MailConf [lolEmail=" + lolEmail + ", emailHost=" + emailHost
				+ ", emailFrom=" + emailFrom + ", emailPassword="
				+ emailPassword + ", emailSubject=" + emailSubject
				+ ", moduleInCharge=" + moduleInCharge + "]";
	}
	
	
}
