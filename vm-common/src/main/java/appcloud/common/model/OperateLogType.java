package appcloud.common.model;

import lombok.ToString;

import java.sql.Timestamp;

/**
 * Created by zouji on 2017/11/6.
 */
public class OperateLogType {

    private String result;
    private String appkeyId;
    private String userId;
    private String device;
    private String provider;
    private String operateType;
    private String deviceId;
    private String infoType;
    private Timestamp createdTime;

    public OperateLogType() {}

    public OperateLogType(String result, String appkeyId, String userId, String device, String provider, String operateType, String deviceId, String infoType, Timestamp createdTime) {
        this.result = result;
        this.appkeyId = appkeyId;
        this.userId = userId;
        this.device = device;
        this.provider = provider;
        this.operateType = operateType;
        this.deviceId = deviceId;
        this.infoType = infoType;
        this.createdTime = createdTime;
    }
    @Override
    public String toString(){
        return "OperateLogType["
                + "result=" + result
                + ", appkeyId=" + appkeyId
                + ", userId=" + userId
                + ", device=" + device
                + ", provider=" + provider
                + ", operateType=" + operateType
                + ", deviceId=" + deviceId
                + ", infoType=" + infoType
                + ", createdTime=" + createdTime
                + "]";
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getAppkeyId() {
        return appkeyId;
    }

    public void setAppkeyId(String appkeyId) {
        this.appkeyId = appkeyId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getDevice() {
        return device;
    }

    public void setDevice(String device) {
        this.device = device;
    }

    public String getProvider() {
        return provider;
    }

    public void setProvider(String provider) {
        this.provider = provider;
    }

    public String getOperateType() {
        return operateType;
    }

    public void setOperateType(String operateType) {
        this.operateType = operateType;
    }

    public String getDeviceId() {
        return deviceId;
    }

    public void setDeviceId(String deviceId) {
        this.deviceId = deviceId;
    }

    public String getInfoType() {
        return infoType;
    }

    public void setInfoType(String infoType) {
        this.infoType = infoType;
    }

    public Timestamp getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Timestamp createdTime) {
        this.createdTime = createdTime;
    }
}
