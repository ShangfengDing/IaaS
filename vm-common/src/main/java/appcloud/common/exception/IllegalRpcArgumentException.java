package appcloud.common.exception;

import appcloud.rpc.tools.RpcException;

public class IllegalRpcArgumentException extends RpcException{

	private static final long serialVersionUID = 1L;

	public IllegalRpcArgumentException(String why) {
		super(why);
	}
	
	public IllegalRpcArgumentException(String why, Object argv) {
		super(why + argv.toString());
	}

}
