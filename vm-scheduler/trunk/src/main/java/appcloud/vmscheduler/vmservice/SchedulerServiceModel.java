public class SchedulerServiceModel {
	
	public static enum BasicOperation {
        CREATE, START, DELETE, STOP, FORCESTOP, RESUME, RESIZE, SUSPEND, REBOOT, GETVMSTATE;

        public String toString() {
            switch (this) {
            case CREATE:
                return "create";
            case START:
                return "start";
            case DELETE:
                return "delete";
            case STOP:
                return "stop";
            case FORCESTOP:
                return "forcestop";
            case RESUME:
                return "resume";
            case RESIZE:
                return "resize";
            case SUSPEND:
                return "suspend";
            case GETVMSTATE:
                return "getVMState";
            }
            return super.toString();
        }
        
    }

}
