public class RemotingSendRequestException extends Exception {

    public String addr;

    public RemotingSendRequestException() {
    }

    public RemotingSendRequestException(String addr) {
        super();
        this.addr = addr;
    }

    public RemotingSendRequestException(String message,String addr) {
        super(message);
        this.addr = addr;
    }

    public RemotingSendRequestException(String message, Throwable cause) {
        super(message, cause);
    }


    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }
}
