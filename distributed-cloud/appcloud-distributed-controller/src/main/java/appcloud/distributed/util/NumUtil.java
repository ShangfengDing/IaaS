package appcloud.distributed.util;

import java.util.Random;

/**
 * Created by Idan on 2018/1/19.
 */
public class NumUtil {

    /**
     * 是否为空
     * @param lo
     * @return
     */
    public static Boolean isEmpty(Long lo) {
        return (lo==null)||(lo==0);
    }

    public static Boolean isNotEmpty(Long lo) {
        return (lo==null)||(lo==0);
    }

    /**
     *  生成的大于等于A 却小于B 的随机数
     */
    public static Integer selectAB(Integer A, Integer B) {
        return (int)(Math.random()*(B-A)+A);
    }
}
