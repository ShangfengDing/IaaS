package appcloud.distributed.service.check;


import org.apache.commons.lang3.StringUtils;

/**
 * Created by Idan on 2018/1/15.
 */
public class AccountChecker {

    public static Boolean createUserCheck(String newUserEmail, String groupSecret, String appkeyId) {
        if (StringUtils.isEmpty(newUserEmail) || StringUtils.isEmpty(groupSecret) || StringUtils.isEmpty(appkeyId)) {
            return false;
        } else {
            return true;
        }
    }

}
